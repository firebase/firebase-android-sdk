        audioTrack.release() // Free
    }

    internal fun setupAudioTrack() {
        val sampleRate = 24000 // Adjust based on server settings
        val channelConfig = AudioFormat.CHANNEL_OUT_MONO
        val audioFormat = AudioFormat.ENCODING_PCM_16BIT
        val minBufferSize = AudioTrack.getMinBufferSize(sampleRate, channelConfig, audioFormat)
        audioTrack = AudioTrack(
            AudioManager.STREAM_MUSIC,
            sampleRate,
            channelConfig,
            audioFormat,
            minBufferSize,
            AudioTrack.MODE_STREAM
        )
        audioTrack.play()
    }

    internal fun playAudio(data: ByteArray) {
        if(!stopRecording) {
            audioTrack.write(data, 0, data.size)
        }
    }
    internal fun startRecording(): Flow<ByteArray> {
        return flow {
            emit(Base64.decode(TEST_64, Base64.NO_WRAP))
        }
        // Define AudioTrack settings
        val recorderSampleRate = 16000 // Adjust based on server settings
        val recorderChannelConfig = AudioFormat.CHANNEL_IN_MONO
        val recorderAudioFormat = AudioFormat.ENCODING_PCM_16BIT

        val bufferSize = AudioRecord.getMinBufferSize(
            recorderSampleRate,
            recorderChannelConfig,
            recorderAudioFormat
        )
        audioRecord = AudioRecord(
            MediaRecorder.AudioSource.VOICE_COMMUNICATION,
            recorderSampleRate,
            recorderChannelConfig,
            recorderAudioFormat,
            bufferSize
