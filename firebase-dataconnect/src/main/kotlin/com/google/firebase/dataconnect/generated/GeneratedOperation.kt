package com.google.firebase.dataconnect.generated

import com.google.firebase.dataconnect.ExperimentalFirebaseDataConnect
import com.google.firebase.dataconnect.OperationRef
import com.google.firebase.dataconnect.copy
import kotlinx.serialization.DeserializationStrategy
import kotlinx.serialization.SerializationStrategy

/**
 * The parent of [GeneratedQuery] and [GeneratedMutation], which are to be implemented by per-query
 * and per-mutation classes, respectively, generated by the Firebase CLI code generation.
 *
 * ### Safe for concurrent use
 *
 * All methods and properties of [GeneratedOperation] are thread-safe and may be safely called
 * and/or accessed concurrently from multiple threads and/or coroutines.
 *
 * ### Stable for inheritance
 *
 * The [GeneratedOperation] interface _is_ stable for inheritance in third-party libraries, as new
 * methods will not be added to this interface and contracts of the existing methods will not be
 * changed, except possibly during major version number changes.
 */
public interface GeneratedOperation<Connector : GeneratedConnector<Connector>, Data, Variables> {

  /** The [GeneratedConnector] with which this object is associated. */
  public val connector: Connector

  /**
   * The name of the operation, as defined in GraphQL.
   * @see OperationRef.operationName
   */
  public val operationName: String

  /**
   * The deserializer to use to deserialize the response data for this operation.
   * @see OperationRef.dataDeserializer
   */
  public val dataDeserializer: DeserializationStrategy<Data>

  /**
   * The serializer to use to serialize the variables for this operation.
   * @see OperationRef.variablesSerializer
   */
  public val variablesSerializer: SerializationStrategy<Variables>

  /**
   * Returns a [OperationRef] that can be used to execute this operation with the given variables.
   */
  public fun ref(variables: Variables): OperationRef<Data, Variables> =
    connector.dataConnect.mutation(operationName, variables, dataDeserializer, variablesSerializer)

  /**
   * Creates and returns a new object that is a _copy_ of this object, but with the properties whose
   * names corresponding to the given arguments changed to the respective argument's value.
   *
   * This function is essentially the same as the `copy()` method that is generated by the Kotlin
   * compiler for `data class` classes.
   *
   * @see withDataDeserializer
   * @see withVariablesSerializer
   */
  @ExperimentalFirebaseDataConnect
  public fun copy(
    connector: Connector = this.connector,
    operationName: String = this.operationName,
    dataDeserializer: DeserializationStrategy<Data> = this.dataDeserializer,
    variablesSerializer: SerializationStrategy<Variables> = this.variablesSerializer,
  ): GeneratedOperation<Connector, Data, Variables>

  /**
   * Creates and returns a new object that is a _copy_ of this object, just like [copy], except that
   * the [variablesSerializer] can be a different type than [Variables].
   *
   * @see copy
   * @see withDataDeserializer
   */
  @ExperimentalFirebaseDataConnect
  public fun <NewVariables> withVariablesSerializer(
    variablesSerializer: SerializationStrategy<NewVariables>,
  ): GeneratedOperation<Connector, Data, NewVariables>

  /**
   * Creates and returns a new object that is a _copy_ of this object, just like [copy], except that
   * the [dataDeserializer] can be a different type than [Data].
   *
   * @see copy
   * @see withVariablesSerializer
   */
  @ExperimentalFirebaseDataConnect
  public fun <NewData> withDataDeserializer(
    dataDeserializer: DeserializationStrategy<NewData>,
  ): GeneratedOperation<Connector, NewData, Variables>

  /**
   * Compares this object with another object for equality.
   *
   * If this methods returns `true` then invoking any method or accessing any property on `this`
   * should have the same side effects as accessing the same property or invoking the same method
   * with the same arguments on `other`, respectively. The implementation checks for _logical_
   * equality, rather than _referential_ equality. A likely implementation looks like this:
   *
   * ```
   * override fun equals(other: Any?): Boolean =
   *   other is DemoConnectorGeneratedQueryImpl<*,*> &&
   *   other.connector == connector &&
   *   other.operationName == operationName &&
   *   other.dataDeserializer == dataDeserializer &&
   *   other.variablesSerializer == variablesSerializer
   * ```
   *
   * @param other The object to compare to this for equality.
   * @return `true` if, and only if, the given object is logically "equal" to this object.
   */
  override fun equals(other: Any?): Boolean

  /**
   * Calculates and returns the hash code for this object.
   *
   * @return the hash code for this object.
   */
  override fun hashCode(): Int

  /**
   * Returns a string representation of this object, useful for debugging.
   *
   * The string representation is _not_ guaranteed to be stable and may change without notice at any
   * time. Therefore, the only recommended usage of the returned string is debugging and/or logging.
   * Namely, parsing the returned string or storing the returned string in non-volatile storage
   * should generally be avoided in order to be robust in case that the string representation
   * changes.
   *
   * @return a string representation of this object, which includes the class name and the values of
   * all public properties.
   */
  override fun toString(): String
}
